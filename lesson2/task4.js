"use strict";

/**
 * @param {number} arg1 
 * @param {number} arg2 
 */
function plus(arg1, arg2)
{
    return arg1 + arg2;
}

/**
 * @param {number} arg1 
 * @param {number} arg2 
 */
function minus(arg1, arg2)
{
    return arg1 - arg2;
}

/**
 * @param {number} arg1 
 * @param {number} arg2 
 */
function mult(arg1, arg2)
{
    return arg1 * arg2;
}

/**
 * @param {number} arg1 
 * @param {number} arg2 
 */
function divis(arg1, arg2)
{
    return arg1 / arg2;
}
